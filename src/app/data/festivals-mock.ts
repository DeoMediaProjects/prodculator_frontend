// Helper function to calculate days until deadline
const calculateDaysUntil = (dateStr: string): number => {
  const target = new Date(dateStr);
  const today = new Date('2026-01-23'); // Current date in the app
  const diffTime = target.getTime() - today.getTime();
  const diffDays = Math.ceil(diffTime / (1000 * 60 * 60 * 24));
  return diffDays;
};

// Helper to determine current status
const determineStatus = (deadlines: Festival['deadlines']): Festival['currentStatus'] => {
  const today = new Date('2026-01-23');
  
  const earlyBird = deadlines.find(d => d.tier === 'early-bird');
  const regular = deadlines.find(d => d.tier === 'regular');
  const late = deadlines.find(d => d.tier === 'late');
  
  if (earlyBird && new Date(earlyBird.date) >= today) return 'early-bird-open';
  if (regular && new Date(regular.date) >= today) return 'regular-open';
  if (late && new Date(late.date) >= today) return 'late-open';
  
  return 'closed';
};

// Helper to get next deadline
const getNextDeadline = (deadlines: Festival['deadlines']) => {
  const today = new Date('2026-01-23');
  const futureDeadlines = deadlines.filter(d => new Date(d.date) >= today);
  if (futureDeadlines.length === 0) return undefined;
  
  return futureDeadlines.reduce((earliest, current) => {
    return new Date(current.date) < new Date(earliest.date) ? current : earliest;
  });
};

